/**
  * op_add - adds two integers
  * @a: the first integer to be added
  * @b: The second integer to be added
  *
  * Return: sum of two integers
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtracts two integers
  * @a: the first integer to be subtracted
  * @b: The second integer to be subtracted
  *
  * Return: difference of two integers
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplies two integers
  * @a: the first integer to be multiplied
  * @b: The second integer to be multiplied
  *
  * Return: product of two integers
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - adds two integers
  * @a: the first integer (divisor)
  * @b: The second integer (dividend)
  *
  * Return: quotient of two integers
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - modulatess two integers
  * @a: the first integer (divisor)
  * @b: The second integer (dividend)
  *
  * Return: remainder of two integers
  */
int op_mod(int a, int b)
{
	return (a % b);
}
